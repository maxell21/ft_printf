int	ft_istype(char c)
{
	if (c == 'c' || c == 's' || c == 'p' ||\
		c == 'd' || c == 'i' || c == 'u' ||\
		c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}

int	ft_pointer(char c)
{
	return (c == 'p');
}

int	ft_inttype(char c)
{
	return (c == 'd' || c == 'i' || c == 'u' \
			|| c == 'x' || c == 'X');
}
